@st.cache_data(ttl=3600)
def load_ctos_from_kml(path: str):
    """
    Carrega CTOs do arquivo KML
    Retorna lista de dicion√°rios com: name, lat, lon, desc
    """
    try:
        namespaces = {'kml': 'http://www.opengis.net/kml/2.2'}
        tree = ET.parse(path)
        root = tree.getroot()
        ctos = []
        
        for placemark in root.findall(".//kml:Placemark", namespaces):
            name_elem = placemark.find("kml:name", namespaces)
            desc_elem = placemark.find(".//kml:value", namespaces)
            coords_elem = placemark.find(".//kml:coordinates", namespaces)
            
            if coords_elem is not None and coords_elem.text:
                parts = coords_elem.text.strip().split(",")
                if len(parts) >= 2:
                    lon, lat = float(parts[0]), float(parts[1])
                    
                    # Validar coordenadas
                    if -90 <= lat <= 90 and -180 <= lon <= 180:
                        ctos.append({
                            "name": name_elem.text.strip() if name_elem is not None else "CTO",
                            "desc": desc_elem.text.strip() if desc_elem is not None else "",
                            "lat": lat,
                            "lon": lon
                        })
        
        logger.info(f"‚úÖ Carregados {len(ctos)} CTOs do arquivo KML")
        return ctos
    except Exception as e:
        logger.error(f"‚ùå Erro ao carregar CTOs do KML: {e}")
        st.error(f"‚ùå Erro ao processar arquivo de CTOs: {e}")
        return []

